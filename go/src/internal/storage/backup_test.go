func TestTableManager_RestoreTableFromBackup(t *testing.T) {
	// Create temporary directory for test databases
	tempDir, err := os.MkdirTemp("", "dyscount-backup-test-*")
	if err != nil {
		t.Fatalf("Failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tempDir)

	tm, err := NewTableManager(tempDir, "default")
	if err != nil {
		t.Fatalf("Failed to create table manager: %v", err)
	}

	// Create test table
	req := &models.DynamoDBRequest{
		TableName: "SourceTable",
		KeySchema: []models.KeySchemaElement{
			{AttributeName: "pk", KeyType: "HASH"},
		},
		AttributeDefinitions: []models.AttributeDefinition{
			{AttributeName: "pk", AttributeType: "S"},
		},
	}

	_, err = tm.CreateTable(req)
	if err != nil {
		t.Fatalf("Failed to create table: %v", err)
	}

	// Create backup
	backupReq := &models.CreateBackupRequest{
		TableName:  "SourceTable",
		BackupName: "SourceBackup",
	}

	backup, err := tm.CreateBackup(backupReq)
	if err != nil {
		t.Fatalf("Failed to create backup: %v", err)
	}

	// Restore table from backup
	restoreReq := &models.RestoreTableFromBackupRequest{
		BackupArn:       backup.BackupArn,
		TargetTableName: "RestoredTable",
	}

	metadata, err := tm.RestoreTableFromBackup(restoreReq)
	if err != nil {
		t.Fatalf("Failed to restore table: %v", err)
	}

	// Verify restored table
	if metadata.TableName != "RestoredTable" {
		t.Errorf("Expected TableName=RestoredTable, got %s", metadata.TableName)
	}

	// Verify table exists
	tables, _ := tm.ListTables()
	found := false
	for _, name := range tables {
		if name == "RestoredTable" {
			found = true
			break
		}
	}
	if !found {
		t.Error("Restored table not found in table list")
	}
}

func TestTableManager_RestoreTableFromBackup_TargetExists(t *testing.T) {
	// Create temporary directory for test databases
	tempDir, err := os.MkdirTemp("", "dyscount-backup-test-*")
	if err != nil {
		t.Fatalf("Failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tempDir)

	tm, err := NewTableManager(tempDir, "default")
	if err != nil {
		t.Fatalf("Failed to create table manager: %v", err)
	}

	// Create source table
	sourceReq := &models.DynamoDBRequest{
		TableName: "SourceTable",
		KeySchema: []models.KeySchemaElement{
			{AttributeName: "pk", KeyType: "HASH"},
		},
		AttributeDefinitions: []models.AttributeDefinition{
			{AttributeName: "pk", AttributeType: "S"},
		},
	}

	_, err = tm.CreateTable(sourceReq)
	if err != nil {
		t.Fatalf("Failed to create source table: %v", err)
	}

	// Create target table (that already exists)
	targetReq := &models.DynamoDBRequest{
		TableName: "TargetTable",
		KeySchema: []models.KeySchemaElement{
			{AttributeName: "pk", KeyType: "HASH"},
		},
		AttributeDefinitions: []models.AttributeDefinition{
			{AttributeName: "pk", AttributeType: "S"},
		},
	}

	_, err = tm.CreateTable(targetReq)
	if err != nil {
		t.Fatalf("Failed to create target table: %v", err)
	}

	// Create backup
	backupReq := &models.CreateBackupRequest{
		TableName:  "SourceTable",
		BackupName: "SourceBackup",
	}

	backup, err := tm.CreateBackup(backupReq)
	if err != nil {
		t.Fatalf("Failed to create backup: %v", err)
	}

	// Try to restore to existing table
	restoreReq := &models.RestoreTableFromBackupRequest{
		BackupArn:       backup.BackupArn,
		TargetTableName: "TargetTable",
	}

	_, err = tm.RestoreTableFromBackup(restoreReq)
	if err == nil {
		t.Error("Expected error when restoring to existing table")
	}
	if !strings.Contains(err.Error(), "already exists") {
		t.Errorf("Expected 'already exists' error, got: %v", err)
	}
}

