func TestTableManager_CreateBackup(t *testing.T) {
	// Create temporary directory for test databases
	tempDir, err := os.MkdirTemp("", "dyscount-backup-test-*")
	if err != nil {
		t.Fatalf("Failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tempDir)

	tm, err := NewTableManager(tempDir, "default")
	if err != nil {
		t.Fatalf("Failed to create table manager: %v", err)
	}

	// Create test table
	req := &models.DynamoDBRequest{
		TableName: "TestTable",
		KeySchema: []models.KeySchemaElement{
			{AttributeName: "pk", KeyType: "HASH"},
		},
		AttributeDefinitions: []models.AttributeDefinition{
			{AttributeName: "pk", AttributeType: "S"},
		},
	}

	_, err = tm.CreateTable(req)
	if err != nil {
		t.Fatalf("Failed to create table: %v", err)
	}

	// Create backup
	backupReq := &models.CreateBackupRequest{
		TableName:  "TestTable",
		BackupName: "TestBackup",
	}

	backup, err := tm.CreateBackup(backupReq)
	if err != nil {
		t.Fatalf("Failed to create backup: %v", err)
	}

	// Verify backup
	if backup.BackupName != "TestBackup" {
		t.Errorf("Expected BackupName=TestBackup, got %s", backup.BackupName)
	}
	if backup.TableName != "TestTable" {
		t.Errorf("Expected TableName=TestTable, got %s", backup.TableName)
	}
	if backup.BackupStatus != "AVAILABLE" {
		t.Errorf("Expected BackupStatus=AVAILABLE, got %s", backup.BackupStatus)
	}
	if backup.BackupArn == "" {
		t.Error("BackupArn should not be empty")
	}
	if backup.BackupSizeBytes == 0 {
		t.Error("BackupSizeBytes should not be 0")
	}
}

