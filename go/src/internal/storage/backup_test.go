func TestTableManager_ListBackups(t *testing.T) {
	// Create temporary directory for test databases
	tempDir, err := os.MkdirTemp("", "dyscount-backup-test-*")
	if err != nil {
		t.Fatalf("Failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tempDir)

	tm, err := NewTableManager(tempDir, "default")
	if err != nil {
		t.Fatalf("Failed to create table manager: %v", err)
	}

	// Create test table
	req := &models.DynamoDBRequest{
		TableName: "TestTable",
		KeySchema: []models.KeySchemaElement{
			{AttributeName: "pk", KeyType: "HASH"},
		},
		AttributeDefinitions: []models.AttributeDefinition{
			{AttributeName: "pk", AttributeType: "S"},
		},
	}

	_, err = tm.CreateTable(req)
	if err != nil {
		t.Fatalf("Failed to create table: %v", err)
	}

	// Create multiple backups
	for i := 0; i < 3; i++ {
		backupReq := &models.CreateBackupRequest{
			TableName:  "TestTable",
			BackupName: "TestBackup" + string(rune('0'+i)),
		}
		_, err := tm.CreateBackup(backupReq)
		if err != nil {
			t.Fatalf("Failed to create backup %d: %v", i, err)
		}
	}

	// List all backups
	listReq := &models.ListBackupsRequest{}
	backups, err := tm.ListBackups(listReq)
	if err != nil {
		t.Fatalf("Failed to list backups: %v", err)
	}

	if len(backups) != 3 {
		t.Errorf("Expected 3 backups, got %d", len(backups))
	}

	// List with limit
	listReq = &models.ListBackupsRequest{Limit: 2}
	backups, err = tm.ListBackups(listReq)
	if err != nil {
		t.Fatalf("Failed to list backups: %v", err)
	}

	if len(backups) != 2 {
		t.Errorf("Expected 2 backups with limit, got %d", len(backups))
	}
}

func TestTableManager_DeleteBackup(t *testing.T) {
	// Create temporary directory for test databases
	tempDir, err := os.MkdirTemp("", "dyscount-backup-test-*")
	if err != nil {
		t.Fatalf("Failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tempDir)

	tm, err := NewTableManager(tempDir, "default")
	if err != nil {
		t.Fatalf("Failed to create table manager: %v", err)
	}

	// Create test table
	req := &models.DynamoDBRequest{
		TableName: "TestTable",
		KeySchema: []models.KeySchemaElement{
			{AttributeName: "pk", KeyType: "HASH"},
		},
		AttributeDefinitions: []models.AttributeDefinition{
			{AttributeName: "pk", AttributeType: "S"},
		},
	}

	_, err = tm.CreateTable(req)
	if err != nil {
		t.Fatalf("Failed to create table: %v", err)
	}

	// Create backup
	backupReq := &models.CreateBackupRequest{
		TableName:  "TestTable",
		BackupName: "ToDelete",
	}

	backup, err := tm.CreateBackup(backupReq)
	if err != nil {
		t.Fatalf("Failed to create backup: %v", err)
	}

	// Verify backup exists
	listReq := &models.ListBackupsRequest{}
	backups, _ := tm.ListBackups(listReq)
	if len(backups) != 1 {
		t.Fatalf("Expected 1 backup before deletion, got %d", len(backups))
	}

	// Delete backup
	deleted, err := tm.DeleteBackup(backup.BackupArn)
	if err != nil {
		t.Fatalf("Failed to delete backup: %v", err)
	}

	if deleted.BackupStatus != "DELETED" {
		t.Errorf("Expected BackupStatus=DELETED, got %s", deleted.BackupStatus)
	}

	// Verify backup is gone
	backups, _ = tm.ListBackups(listReq)
	if len(backups) != 0 {
		t.Errorf("Expected 0 backups after deletion, got %d", len(backups))
	}
}

